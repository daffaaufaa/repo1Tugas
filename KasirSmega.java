    private void sum() {
     //To change body of generated methods, choose Tools | Templates.
        DefaultTableModel dataModel = (DefaultTableModel) tblpenjualan.getModel();
        int jumlah = dataModel.getRowCount();
        int sumBerat =0;
        int sumTinggi =0;
        int sumTotal =0;
        
        for (int i = 0; i < jumlah; i++) {
            int dataBerat = Integer.valueOf(dataModel.getValueAt(i, 1).toString());
            int dataTinggi = Integer.valueOf(dataModel.getValueAt(i, 2).toString());
            int dataTotal = Integer.valueOf(dataModel.getValueAt(i, 3).toString());
            sumBerat += dataBerat;
            sumTinggi += dataTinggi;
            sumTotal += dataTotal;
        }
        //jumlah_data.setText(Integer.toString(jumlah));
        //sum_berat.setText(Double.toString(sumBerat));
        //sum_tinggi.setText(Double.toString(sumTinggi)); 
        lbltotal.setText(Integer.toString(sumTotal));
       // txtdata.setText(String.valueOf(sumBerat1));
    }

