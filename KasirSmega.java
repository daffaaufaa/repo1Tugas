    private void txtjmlKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtjmlKeyPressed
        // TODO add your handling code here:
         DefaultTableModel dataModel = (DefaultTableModel)tblpenjualan.getModel(); 
        List list = new ArrayList<>();
        tblpenjualan.setAutoCreateColumnsFromModel(true);
        list.add(cmbbarang.getSelectedItem().toString());
        list.add(lblharga.getText());
        list.add(txtjml.getText());
        
        if(evt.getKeyCode()== KeyEvent.VK_ENTER) {
            if(baristerpilih) {
                 btnEditClick();
         }else{
        subtotal();
        list.add(String.valueOf(subtotalbelanja));
        dataModel.addRow(list.toArray());
        bersihkanlayar();
        total();
        //sum();
    }
        }
       if(evt.getKeyCode()== KeyEvent.VK_DELETE){
           if(baristerpilih){
             int baris = tblpenjualan.getSelectedRow();
             dataModel.removeRow(baris);
               KeyDelete();
           }
        tblpenjualan.clearSelection();
        baristerpilih=false;
      }
            
    }//GEN-LAST:event_txtjmlKeyPressed

